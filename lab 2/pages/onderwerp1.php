Minecraft is geschreven in Java en het werd aanvankelijk ontwikkeld door Markus Persson en tegenwoordig door het door hem opgerichte bedrijf, Mojang AB. De officiële versie is in Nederland uitgekomen op 18 november 2011. Het werd gepresenteerd tijdens een conventie voor Minecraft-spelers, genaamd MineCon 2011.

Een van de manieren om Minecraft te spelen is via de modus "Survival", wat inhoudt dat de speler in een bepaalde wereld moet zien te overleven. Om in leven te blijven moet de speler op zoek gaan naar voedsel, dat verkregen kan worden door bijvoorbeeld dieren te slachten of graan en groente te verbouwen. Ook moet de speler zich verdedigen tegen vijandelijke mobs die 's nachts ontstaan en de speler proberen te doden. In de survivalmodus zal de speler zelf bouwmaterialen moeten verzamelen door het omhakken van bomen en het uithakken van gesteenten.<br><br>
